package ex1;

public class Calcul {

    /**
     * Faire l'addition
     *
     * @param a int
     * @param b int
     * @return int
     */
    public int addition(int a, int b) {
        return a + b;
    }

    /**
     * Faire la soustriction
     *
     * @param a
     * @param b
     * @return
     */
    public int soustriction(int a, int b) {
        return a - b;
    }
}
